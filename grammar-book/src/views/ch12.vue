<h1 id="possessive-s">Possessive ’s</h1>
<p>This chapter discusses the correct way to use possessive -’s. In this
context, the single quote symbol (’) is called an <span
class="smallcaps">apostrophe</span> (pronounced /a-POS-tro-fee/). You
can refer to possessive ’s as <em>apostrophe s</em>.</p>
<p>We use <span class="smallcaps">’s</span> to indicate the things that
people possess as well as their attributes:</p>
<div class="compactitem">
<p><em>My wife’s car</em></p>
<p><em>The president’s schedule</em></p>
<p><em>Melinda’s smile</em></p>
</div>
<p>In general, the use of possessive -s for people’s possessions is
required, not optional.</p>
<p>Possessive ’s is also used with names that end in -s:</p>
<div class="compactitem">
<p><em>Are you going to Mr. Jones’s farewell party?</em></p>
<p><em>Have you seen Chris’s dog?</em></p>
</div>
<p>The repetition of an ’s’ at the end of the word is not problematic:
to pronounce the possessive ’s’, say /iz/. <em>Chris’s</em> is
pronounced /KRIS-iz/.</p>
<p>Though rare, it is possible to make chains of two possessive ’s :
<em>Chris’s girlfriend’s car</em></p>
<p>However, there are common sense limits to the use of possessive -’s
for people. For instance, we would not use possessive -s following a
relative clause: . In this case it is obviously preferable to say:</p>
<blockquote>
<p><em>Here is the number of the man who phoned you.</em></p>
</blockquote>
<p>We use also <span class="smallcaps">’s</span> with groups of people,
such as companies, organizations, associations, teams, countries, and
cities:</p>
<div class="compactitem">
<p><em>Nestle’s headquarters</em></p>
<p><em>Fribourg’s old town</em></p>
<p><em>Brazil’s exports</em></p>
<p><em>the HEG’s budget</em></p>
</div>
<p>It is also possible to use <span class="smallcaps">’s</span> for the
attributes of things, though it is not required. In fact, when
describing the attibures of things, the use of <span
class="smallcaps">of</span> is probably more common than possessive
’s.</p>
<div class="compactitem">
<p><em>The planet’s rings</em> is fine, but so is <em>the rings of the
planet</em></p>
<p><em>The building’s location</em> is fine, but so is <em>the location
of the building</em></p>
<p><em>The screen’s resolution</em> is fine, but so is <em>the
resolution of the screen</em></p>
</div>
<p>Keep in mind, however, that it is often possible (and desirable) to
omit the possessive in favor of a compound noun. As explained in <a
href="#e0141" data-reference-type="ref"
data-reference="e0141">[e0141]</a>, compound nouns are often a more
elegant solution than possessive ’s or prepositions.</p>
<div class="compactitem">
<p><em>the book cover</em> is often better than ’the book’s cover’ or
’the cover of the book’</p>
<p><em>the computer screen</em> is often better than ’the computer’s
screen’ or ’the screen of the computer’</p>
<p><em>tourist guide</em> is better than ’tourists’ guide’ or ’the guide
for tourists’</p>
</div>
<p>Sometimes the preposition <span class="smallcaps">of</span> is
considered to ’sound better’. For example, most people would probably
agree theat the phrase ’The sound of rain’ sounds better than ’rain’s
sound’ (due to the relative difficulty of saying the two ’s’ sounds in
’rain’s sound’). For this and other reasons, <span
class="smallcaps">of</span> is often used in book titles, slogans, and
idioms.</p>
<p>In the end, choosing between <span class="smallcaps">of</span> and
possessive ’s in these cases is often a stylistic question of rhythm and
sound.</p>
<p>We also commonly use -’s with time expressions:</p>
<div class="compactitem">
<p><em>Next week’s meeting is cancelled</em></p>
<p><em>Do you have yesterday’s newspaper?</em></p>
<p><em>I will see you in three weeks’ time!</em></p>
</div>
<p>Finally, when the possessing subject is a plural noun ending in -s,
we just add an apostrophe (no need for an ’s’).</p>
<div class="compactitem">
<p>my friends’ house (a house belonging to more than one friend)</p>
<p>the players’ equipment (equipment belonging to more than one
player)</p>
</div>
<p>refers to an unusual or unnatural use of possessive ’s.</p>
