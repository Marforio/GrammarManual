<h1 id="verb-tenses-and-modalities">Verb tenses and modalities</h1>
<p>In the last chapter, we reviewed the rules of conjugation that encode
both the intention of the speaker (declarative, negative, interrogative)
and the nature of the agent, that is the kind of subject that performs
the action.</p>
<p>This chapter deals with the difficulty of choosing the right tense or
modality to fit the situation. We have a variety of options depending on
the time (past, present, future) or type of interaction (permission,
obligation, recommendation, etc.) in our situation.</p>
<h2 id="tenses-and-modalities">Tenses and modalities</h2>
<p><span class="smallcaps">Tense</span> refers to the time of the action
(past, present, future). <span class="smallcaps">Modality</span> refers
to the degree of obligation or force applied to the action by the
speaker (obligatory, probable, recommended, possible, etc).</p>
<p>We create tenses and modalities when we combine an auxiliary verb
with a principal verb. As we saw in REF, there is a limited number of
auxiliary verbs. The most important ones are <em>be, do, have, will,
can, must, should, could, would, might and may</em>. Principal verbs
potentially infinite in number, but they come in four forms (infinitive,
past simple, past participle and gerund), as shown in ref.</p>
