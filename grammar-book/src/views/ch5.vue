<h1 id="past-modals">Past modals</h1>
<h2 id="should-and-could">Should and could</h2>
<p>The past forms of the modal verbs “should” and “could” allow us to
criticize past actions and situations. They are useful when making a
self-evaluation, for example.</p>
<p>The past form of these modal verbs consists of:</p>
<blockquote>
<p><span class="smallcaps">Should/could + not + past
participle</span></p>
</blockquote>
<p>Fig. <a href="#fig:past-modals-should" data-reference-type="ref"
data-reference="fig:past-modals-should">1.1</a> demonstrates how to
create the past form of should and could.</p>
