<h2 id="might-and-must">Might and must</h2>
<p>The past form of might and must is also made by adding <span
class="smallcaps">have</span> and the past participle:</p>
<p>These forms are used to make deductions or guesses about past events.
Example:</p>
<blockquote>
<p><em>- Where is Joe? - I don’t know. He must have gone out for a
smoke. Or he might have gone home.</em></p>
</blockquote>
<p>In the second example, notice that the past form of <span
class="smallcaps">must</span> is not being used to express obligation in
the past. The conjugation <em>must have left</em> is making a guess, not
describing an obligation.</p>
<h5 id="obligation-in-the-past-and-future">Obligation in the past and
future</h5>
<p>To describe obligations in a past situation we cannot use <span
class="smallcaps">must</span>. Instead, we use the expression <span
class="smallcaps">have to + infinitive</span>. Example:</p>
<blockquote>
<p>I had to drive my sister to the hospital yesterday.</p>
</blockquote>
<p>The same rule applies to future time. An obligation in the future can
be described as follows:</p>
<blockquote>
<p>Oh no, I forgot to buy salad! I will have to go the supermarket
again.</p>
</blockquote>
<h2 id="will">Will</h2>
<p>As you know, <span class="smallcaps">will + infinitive</span> is used
to talk about the future: <em>I will finish my degree next
year.</em></p>
<p>It is sometimes necessary to talk about the future from the
perspective of the past, especially when telling stories or reporting
information. Example:</p>
<blockquote>
<p><em>…and the policeman said that [he will let me go this
time].</em></p>
</blockquote>
<p>In this example we have a future action (“he will let me go”) as seen
from inside a past situation (“the policeman said”). In cases like this,
we have to replace ’will’ with <span class="smallcaps">would</span>, as
follows:</p>
<blockquote>
<p><em>…and the policeman said that he would let me go this
time.</em></p>
</blockquote>
<p>To put it simply, the past form of the auxiliary verb “will” is
would.</p>
<h2 id="can">Can</h2>
<p>To make the past of <span class="smallcaps">can</span>, we simply
replace it with <span class="smallcaps">could</span>:</p>
<blockquote>
<p><em>I could dance well when I was young.</em></p>
</blockquote>
