<h1 id="passive-voice">Passive voice</h1>
<p>Verb conjugation has a dimension that was not mentioned in chapter 3,
when the most important rules of conjugation were discussed. This third
dimension (after (1) the subject and (2) the sentence type) is called
<span class="smallcaps">voice</span>.</p>
<p>The concept of voice is used to explain the changes that occur in
conjugation when the agent of an action is replaced by the object of the
action in the position of the subject. In active voice, the agent is in
the position of the subject: <em>I ate the sandwich</em>. In passive
voice, by contrast, the recipient of the action, not the agent, is in
the position of the subject: <em>The sandwich was eaten by me.</em></p>
<p>The passive voice is not limited to the past tense. Every tense has
active and passive voice, as shown in Figure <a href="#fig:passive"
data-reference-type="ref" data-reference="fig:passive">1.1</a>.</p>
<figure id="fig:passive">
<img src="../images/passive.png" style="width:80.0%" />
<figcaption>Passive voice across tenses</figcaption>
</figure>
<p>As we see in these examples, the passive voice places the object of
the action in the position of the subject, relegating the agent to the
predicate. The passive voice therefore emphasizes the product of an
action, and deemphasizes the agent. For this reason, we often use the
passive to describe fabrication, scientific and engineering processes.
Passive voice is commonly employed in scientific writing.</p>
<p>We also see in fig. <a href="#fig:passive" data-reference-type="ref"
data-reference="fig:passive">1.1</a> that passive voice always contains
a) some for of the verb BE and b) a past participle (3rd form of the
principal verb: see fig. <a href="#fig:principal-verb-forms"
data-reference-type="ref"
data-reference="fig:principal-verb-forms">[fig:principal-verb-forms]</a>
for more information). The verb BE occupies the position of the
principal verb in the original active sentence.</p>
<p>To mention the agent of the action (the original subject) in a
passive sentence, use the preposition <span class="smallcaps">by</span>.
Example:</p>
<blockquote>
<p><em>The electric light bulb was invented by Thomas Edison.</em></p>
</blockquote>
<p>There is a common sense limit to the passive voice. Since the passive
voice involves a swapping of the agent and the object of the action, it
follows logically that it can only be applied to transitive verbs, that
is, verbs that use both an agent and an object. Examples of transitive
verbs include <em>make, eat, buy, give</em> and many more. They are
transitive because we typically make, eat, buy and give
<em>something</em>. Intransitive verbs, on the other hand, do not have
an object, which is why it is impossible to use passive voice on
conjugations such as <em>I slept peacefully</em> or <em>She meditated
for an hour</em>. For more information about transitive and intransitive
verbs, see <a href="#sec:transitive-vs-intransitive"
data-reference-type="ref"
data-reference="sec:transitive-vs-intransitive">[sec:transitive-vs-intransitive]</a>.</p>
