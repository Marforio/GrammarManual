<h2 id="relative-pronouns">Relative pronouns</h2>
<p>There is actually an additional set of pronouns not mentioned in
figure <a href="#fig:pronouns" data-reference-type="ref"
data-reference="fig:pronouns">1.1</a>.</p>
<p>The relative pronouns <span class="smallcaps">who, which, that and
whose</span> have the job of replacing an already-mentioned noun at the
beginning of a new clause that gives further information. Relative
pronouns help us avoid repetitions of this sort:</p>
<blockquote>
<p><em>Michael is a determined character. Michael doesn’t take no for an
answer.</em></p>
</blockquote>
<p>Instead of repeating <em>Michael</em>, we can connect both sentences
using a relative pronoun.</p>
<blockquote>
<p><em>Michael is a determined character who doesn’t take no for an
answer.</em></p>
</blockquote>
<p>In this second example, <span class="smallcaps">who</span> is the
relative pronoun replacing <em>Michael</em> and heading the relative
clause <em>who doesn’t take no for an answer</em>.</p>
<p>Relative clauses provide further information about a person or thing
already identified in the main clause.</p>
<p>English grammar has simple rules for the use of relative
pronouns.</p>
<h5 id="that">THAT</h5>
<p><span class="smallcaps">That</span> is the most flexible of the
relative pronouns. You can use <span class="smallcaps">that</span> to
refer to both people and objects, and even with times:</p>
<div class="compactitem">
<p>refer to people: <em>One man that revolutionized marketing was Steve
Jobs.</em></p>
<p>refer to things: <em>Many frog species only live on trees that are
only found in this part of the jungle.</em></p>
<p>with <em>the day, the year, the time</em>: <em>1983 was the year that
they got married; The bungee jump was the only time that I feared for my
life.</em></p>
</div>
<h5 id="who">WHO</h5>
<p>This relative pronoun is used exclusively with persons or with
personified entities such as teams or companies.</p>
<div class="compactitem">
<p><em>The teacher who helped me had never been kind to me
before.</em></p>
</div>
<p>Be careful not to mistake English <span class="smallcaps">who</span>
for French <em>où</em>.</p>
<h5 id="which">WHICH</h5>
<p>We use <span class="smallcaps">which</span> exclusively to refer to
things: <em>This is an insult which I will not soon forget</em>. In this
example, ’which’ can and often is replaced by ’that’, with no difference
in meaning. However, the use of which is obligatory when the clause is
non-defining, as in this example: <em>"Cattle-related emmissions, which
is the second biggest source of air pollution, is rarely
discussed."</em></p>
<h5 id="whose">WHOSE</h5>
<p>Whose replaces a possessive adjective such as <em>her</em> or
<em>his</em>: <em>She is the engineer whose research I told you
about.</em> (<em>whose</em> replaces ’her’ research).</p>
<p>refers to the inability to use the relative pronoun <span
