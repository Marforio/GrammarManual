<h1 id="pronouns">Pronouns</h1>
<p>The function of a pronoun is to replace a noun or noun phrase. A
group of individuals speaking can be replaced by <em>We</em>; a woman
can be referenced as <em>she</em> and an object can be referred to as
<em>it</em>. This form can change depending on whether the pronoun is
the subject of the sentence or an object, or whether it replaces a
possessive phrase.</p>
