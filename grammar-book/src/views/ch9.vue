<h2 id="no-gender-for-objects">No gender for objects</h2>
<p>Unlike other European languages, English does not apply a gender
(masculine or feminine) to objects such as cars, houses, trees and
everything else. We always use <span class="smallcaps">it</span> to
refer to such objects. It is a common error to use a masculine or
feminine pronoun to refer to a thing.</p>
<h2 id="possessive-adjectives-and-pronouns">Possessive adjectives and
pronouns</h2>
<p>The possessive pronouns are <span class="smallcaps">mine, yours, his,
hers, ours, and theirs</span>. These pronouns replace a possessive noun
phrase such as <em>John’s car</em> or <em>the company’s budget</em>.</p>
<p><span class="smallcaps">my, your, his, her, its, our and your</span>
are technically not pronouns. They are actually possessive adjectives,
because they modify the noun rather than replace it: <em>my car, your
money, his life, her music, our future</em>.</p>
<p>Using possessive pronouns and adjectives can be difficult due to an
important difference between English on the one hand and French and
Italian on the other. This difference involves the third person singular
possessive adjectives and pronouns, i.e. <em>his, her(s) and
its</em>.</p>
<p>In English, the gender of the possessive adjective or pronoun (his,
her(s), its) is determined by the owner, i.e. the possessing agent.</p>
<blockquote>
<p>John is a man with a computer and a house. His computer is new. His
house is old.<br />
Mary is a woman with a computer and a house. Her computer is old. Her
house is new.</p>
</blockquote>
<p>You can see that the adjective is always <span
class="smallcaps">his</span> when John is the owner, and <span
class="smallcaps">her</span> when Mary is the owner.</p>
<p>French works differently. In French, the gender of the pronoun comes
not from the owner, but from the gender of the object itself:</p>
<blockquote>
<p>Jean est un homme. Son ordinateur est nouveau, mais sa maison est
vieille.<br />
Marie est une femme. Son ordinatuer est nouveau, mais sa maison est
nouvelle.</p>
</blockquote>
<p>As you can see, the adjective changes from <em>son</em> to
<em>sa</em> as a function of the gender of the object. The adjective
does not tell us anything about the gender of the owner.</p>
<p>This difference often leads to confusion in the use of possessive
adjectives and pronouns.</p>
